package Desafios.FiltrarCarros;

public class Carro {

    final String nome;
    final String marca;
    final double preco;
    final int potencia;

    public Carro(String nome, String marca, double preco, int potencia) {
      this.nome = nome;
      this.marca = marca;
      this.preco = preco;
      this.potencia = potencia;
    }
}
